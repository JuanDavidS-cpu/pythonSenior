# #

num1 = 6
num2 = 8
#el str() sirve para castear un parametro, o cambiarlo de tipo a cadena de texto
print ("el numero uno es" + str(num1) + "y el numero dos es "+ str(num2) )
# suma
suma = num1 + num2
print("el resultado de la suma es ", suma )

# resta
resta = num1 - num2
print("el resultado de la resta es ", resta )


# multiplicacion 
multiplicacion = num1 * num2
print("el resultado de la multiplicacion es ", multiplicacion )


# division
division = num1 / num2
print("el resultado de la division es ", division )


# division entera 
#  la division entera eliminina la parte decimal y deja la parte entera del resultado de la division
divisionEnte = num1 // num2
print("el resultado de la division entera es ", divisionEnte)


# potencia
potencia = num1 ** num2
print("el resultado de la potencia es ", potencia)


# modulo
modulo = num1 % num2
print ("el resultado del modulo es ", modulo)


